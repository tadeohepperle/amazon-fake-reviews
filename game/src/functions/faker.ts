function stringHashCode(str: string): number {
  var hash = 0,
    i,
    chr;
  if (str.length === 0) return hash;
  for (i = 0; i < str.length; i++) {
    chr = str.charCodeAt(i);
    hash = (hash << 5) - hash + chr;
    hash |= 0; // Convert to 32bit integer
  }
  return hash;
}

export function pseudoRandomName(seed: string): string {
  let hash = stringHashCode(seed);
  return (
    preNames[hash % preNames.length] + " " + lastNames[hash % lastNames.length]
  );
}

export function pseudoRandomDate(seed: string): Date {
  let start = Date.parse("2010-01-01");
  let end = Date.parse("2020-01-01");
  return new Date(
    start.valueOf() + Math.random() * (end.valueOf() - start.valueOf())
  );
}

